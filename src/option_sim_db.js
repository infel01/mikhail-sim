function parts_load(element) {
    let s_parts_id;
    let e_parts_id;

    switch(element) {
        case 'main':
        case 'sub':
        case 'r_spare':
        case 'l_spare':
            s_parts_id = 1;
            e_parts_id = 12;
            break;

        default:
            s_parts_id = -1;
            break;
    }

    for(i = 0 ; i < master_parts_list.length ; i++) {
        if(s_parts_id == -1 && master_parts_list[i].value == element || i >= s_parts_id && i <= e_parts_id) {
            let add_select = document.createElement('option');
            add_select.value = master_parts_list[i].id;
            add_select.textContent = master_parts_list[i].text;
        
            document.getElementById(element).appendChild(add_select);
        }
    }
}